            <div style="border: solid 2px black; text-align: center;font-size: 20px;background-color: yellow;"> Você já parou para pensar na quantidade de doenças dos olhos que existem? <br>
                Apesar do número elevado de problemas   que podem afetar nossa visão,<br>  só
                costumamos tomar alguma ação quando ela já está prejudicada de cerca forma.</div>

            <div><h3>As 19 doenças dos olhos mais comuns que existem </h3>



            </div>
            <div><h3> 1. Ambliopia</h3>


                Também chamada de "olho preguiçoso", a ambliopia é uma disfunção oftalmológica <br> 
                sem lesão aparente. <br>
                Ela geralmente acontece em crianças e é caracterizada como uma doença que<br>
                prejudica a visão, mas que não pode ser corrigido somente com o uso de óculos.<br>
                O tratamento é demorado, pois muitas vezes o problema não está restrito ao olho <br>
                afetado, e sim à região cerebral responsável pela visão.<br>
                Por afetar principalmente crianças, é comum que casos de ambliopia passem <br>
                despercebidos pelos pais. Mas se não for tratado antes dos 7 ou 8 anos de idade, a doença<br>
                pode levar a um déficit de visão que durará pela vida toda. Por isso,acompanhamento <br>
                oftalmológico é fundamental até mesmo nos primeiros anos de vida.
            </div>

            <div><h3> 2. Astigmatismo</h3>


                O astigmatismo é uma das doenças dos olhos mais comuns que existem. Ele é resultado <br> 
                de um problema na formação da córnea, que faz com que a imagem que deveria ser formada na retina<br>
                seja formada em diversas regiões diferentes do olho, resultando numa visão distorcida.<br>
                Isso pode ser tanto um problema que acontece naturalmente, quanto um problema<br>
                associadoa algum trauma que a pessoa tenha sofrido no olho, como uma cicatrização, que altera a <br>
                refração da luz.<br>
                Além do uso de óculos (recomendado para a maioria dos casos), é possível realizar um<br>
                transplante de córnea para lidar com casos mais sérios.


            </div>
            <div><h3> 3. Blefarite</h3>


                A blefarite é uma inflamação nas pálpebras. Sua manifestação e severidade dependem <br>
                bastante de cada pessoa, mas quem tem pele muito oleosa ou tendência a desenvolver caspas, por <br>
                exemplo, pode sofrer com a blefarite.<br>
                Mas o que blefarite tem a ver com caspa? A doença causa uma irritação na base dos cílios,<br>
                onde acabam sendo produzidas escamas muito semelhantes às caspas.<br>
                Essa irritação e incômodo constantes podem afetar o resto do olho, portanto é fundamental<br>
                tratá-la com limpezas recorrentes, compressas de água morna ou remédios receitados pelo médico.<br>
                Eventualmente, xampus com pH neutro podem dar conta do recado também.<br>
                Lentes de contato sujas também podem causar blefarite. Então, caso você use lentes, <br>
                mantenha-as sempre bem limpas.

            </div>

            <div><h3> 4. Catarata</h3>


                A catarata é uma doença bastante associada a idosos, pois a maioria das pessoas com mais<br>
                de 70 anos de idade sofre deste problema.<br>
                É possível que ela apareça em outras idades, mas nesses casos ela costuma estar mais<br>
                relacionada a causas congênitas ou enfermidades como diabetes e traumas no olho.<br>
                A catarata se caracteriza pela perda de transparência do cristalino, que é uma espécie de <br>
                lente natural que temos dentro do olho. Isso ocorre lentamente e pode acontecer em somente um dos olhos<br>
                primeiro, mas a tendência é que, sem tratamento, a catarata vá prejudicando a visão pouco a pouco,<br>
                deixando a imagem mais embaçada.<br>
                Assim, a cirurgia é o único tratamento eficaz contra a doença.

            </div>

            <div><h3> 5. Ceratocone</h3>


                O ceratocone é uma doença geralmente hereditária, que causa deformação da parte central da <br>
                córnea (a superfície transparente que recobre os olhos).<br>
                Ela também pode se manifestar em pessoas alérgicas que coçam demais os olhos. Esse hábito <br>
                pode fazer com que a retina fique mais elevada do que o normal, dando a impressão de que está “saltada”.<br>
                A pessoa com ceratocone costuma ter sua visão debilitada de diversas formas: pode sofrer <br>
                perda do foco, diminuição da visão noturna, sentir alta sensibilidade à luz, entre outras situações. Ela <br>
                também pode ser caracterizada por um amento considerável de astigmatismo em um olho em comparação ao outro.<br>
                Adolescentes e jovens têm mais chance de desenvolver a doença, de modo que ela raramente se <br>
                manifesta após os 30 anos de idade. Óculos e lentes de contato costumam resolver o problema, mas o <br>
                oftalmologista também pode ter de realizar algum procedimento cirúrgico, dependendo do caso.<br>
                O acompanhamento médico também é fundamental, pois o ceratocone não controlado pode levar <br>
                eventualmente à perda de visão.

            </div>
            <div><h3>6. Conjuntivite</h3>


                A conjuntivite é uma inflamação da conjuntiva, uma membrana que recobre as pálpebras e os olhos. <br>
                Quando os olhos estão vermelhos, invariavelmente é sinal de que essa membrana está irritada, <br>  
                portanto trate-se de alguma forma de conjuntivite.<br>
                Obviamente, o nível de irritação varia, assim como as causas: pode ser bacteriana, viral ou alérgica. <br>
                No caso da viral e bacteriana, ela é contagiosa, especialmente porque causa bastante lacrimação e a <br>
                pessoa geralmente limpa os olhos com as mãos.<br>
                O tratamento da conjuntivite viral é apenas sintomático. O da bacteriana pode ser feito com o uso de <br>
                antibióticos receitados pelo médico.<br>
                No caso da conjuntivite alérgica, ela não é contagiosa, mas incomoda pela coceira constante nos olhos.

            </div>
            <div><h3> 7. Daltonismo</h3>


                O daltonismo é uma doença quase sempre hereditária, que dificulta a diferenciação entre as cores <br>
                verde e vermelho. Por vezes, também afeta a percepção do azul e amarelo, ou pode deixar a visão <br>
                acromática, ressaltando somente tons de preto e branco.<br>
                É raro o daltonismo afetar as mulheres em razão de sua composição cromossômica, mas a doença está <br>
                longe de ser um fenômeno raro. Estimativas dão conta de que o problema está presente em cerca de <br>
                5% da população mundial.


            </div>
            <div><h3> 8. Descolamento de retina</h3>


                A luz que entra em nosso olho é refratada de maneira a formar as imagens que enxergamos sobre a <br>
                retina, que é quem “manda” essas imagens para o cérebro por meio do nervo óptico, onde a visão é <br>
                processada.<br>
                A retina é uma membrana que se encontra dentro do olho, e ali ela recebe os nutrientes necessários<br>
                para se manter funcionando. Quando ocorre o descolamento – por trauma, infecções, idade ou doenças – <br>
                a retina para <br>
                de receber estes nutrientes e começa a se debilitar.<br>
                Se não for tratado com urgência, o descolamento de retina pode resultar em cegueira. Como esse<br> 
                descolamento não causa dor, a forma de percebê-lo é por meio da visão turva, pontos e manchas <br>
                escuras na visão (chamados de moscas volantes) ou fotopsias (flashes de luz repentinos). <br> O
                tratamento é, de forma geral, cirúrgico.
            </div>

            <div><h3>9. Degeneração macular relacionada à idade (DMRI)</h3>


                A degeneração macular relacionada à idade, também chamada de DMRI, acomete habitualmente pessoas  <br>
                com mais de 60 anos de idade. <br>
                Ela se dá pela degeneração da área central da retina, causando problemas na visão central. É  <br>
                possível frear um pouco a evolução da doença por meio de acompanhamento nutricional e pela  <br>
                utilização de óculos,  <br>
                mas os danos já provocados por ela são geralmente irreversíveis. <br>

            </div>
            <div><h3>10. Estrabismo</h3>          

                Popularmente chamadas de “vesgas”, as pessoas com estrabismo sofrem a falta de paralelismo dos olhos. <br>
                Um dos olhos pode estar esteja mais para dentro, mais para fora, ou pode ser até que haja uma diferença  <br>
                vertical entre eles. <br>
                O estrabismo pode ser constante ou ocorrer intermitentemente. A pessoa estrábica comumente  <br>
                sofre com dores de cabeça, torcicolos e eventualmente pode ter até perda parcial da visão. <br>
                As causas variam bastante e podem até ser neurológicas. É possível corrigir o estrabismo com  <br>
                óculos, mas condições mais agudas requerem uma operação, que costuma resolver bem o problema. <br>

            </div>
            <div><h3> 11. Glaucoma</h3>            

                O glaucoma é uma das doenças dos olhos mais graves que existem. Ela acontece quando o nervo <br>
                óptico, que liga os olhos ao cérebro, sofre danos constantes em razão do aumento da pressão intra-ocular.<br>
                Essa hipertensão pode ter diversas causas, mas o importante é que, se não tratada, pode levar à cegueira. <br>
                Ela costuma atingir pessoas de idade mais avançada, e o problema é que muitas vezes é assintomática.<br>
                Por isso, é necessário realizar exames com regularidade no oftalmologista, especialmente após os <br>
                60 anos de idade.
            </div>

            <div><h3>  12. Hipermetropia</h3>   


                A hipermetropia ocorre por um defeito anatômico, com os olhos apresentando medidas menores que as usuais. <br>
                Isso faz com que a imagem trazida pela luz se forme depois da retina. Assim, a pessoa com essa condição <br>
                tem dificuldade para enxergar de perto, e esse esforço pode causar dores de cabeça recorrentes.<br>
                Todos estão sujeitos à hipermetropia, inclusive crianças em fase de crescimento. Nesses casos, porém, é <br>
                provável que <br>
                o grau diminua na adolescência, pois os olhos crescem e corrigem um pouco o problema.<br>

            </div>

            <div><h3> 13. Miopia</h3>   


                Assim como a hipermetropia, a miopia também ocorre quando a pessoa apresente um defeito anatômico nos olhos.<br>
                Provavelmente, ela é a primeira palavra que vem à mente quando pensamos em problemas de visão, já que sua <br>
                incidência é grande.<br>
                Os olhos de pessoas com miopia possuem um formato mais alongado ou apresentam má formação das córneas ou <br>
                do cristalino, de maneira que a imagem da visão se forme antes da retina. Isso faz com que essas pessoas <br>
                tenham dificuldade para enxergar de longe, mas conseguirão ver normalmente de perto.<br>
                Neste caso, ela não precisará de óculos para ler, por exemplo, mas talvez precise para enxergar a lousa<br> 
                na escola ou ir a alguma peça de teatro.<br>
                É comum a miopia surgir na infância e na adolescência, mas adultos e idosos também podem desenvolvê-la, <br>
                especialmente quando ela está associada à catarata neste último grupo.<br>

            </div>

            <div><h3> 14. Presbiopia ou vista cansada</h3>   


                A presbiopia é um problema relacionado à idade e também é conhecida como “vista cansada”. Com o passar dos <br>
                anos, o cristalino, que é o que permite aos olhos ajustar o foco da visão, perde sua elasticidade.<br>
                Por isso, pessoas acima dos 40 anos podem desenvolver o problema naturalmente, sendo que o principal sintoma<br>
                é a dificuldade para enxergar de perto.<br>
                Aos poucos, a leitura pode se tornar desagradável, pois o indivíduo precisa afastar os objetos cada vez <br>
                mais para enxergá-los com clareza. A única forma de lidar com o problema é por meio da utilização de óculos.<br>

            </div>
            <div><h3>  15. Pterígio </h3>   


                O pterígio ocorre quando há um espessamento demasiado da conjuntiva (membrana que reveste os olhos). Esse  <br> 
                espessamento acontece a partir da extremidade do olho em direção à córnea..<br>
                Apesar de ser um problema benigno e não infeccioso, se ele cobrir a pupila pode causar cegueira. Acredita-se <br>
                que a incidência constante de raios ultravioleta nos olhos seja uma das causas do pterígio, por isso é <br>
                necessário sempre proteger os olhos com óculos escuros.<br>
                O tratamento para o pterígio é apenas cirúrgico.

            </div>

            <div><h3>  16. Retinopatia</h3>   


                São lesões que atingem a retina e especificamente seus vasos sanguíneos. De maneira geral, elas são<br>
                associadas a doenças sistêmicas que causam essa condição. Um exemplo é a retinopatia diabética, que <br>
                danifica os vasos por causa do excesso de glicose no sangue, comum a pessoas que têm diabetes.<br>
                Em qualquer caso, porém, a retinopatia pode levar à perda da visão e precisa ser tratada antes de o <br>
                problema evoluir para um quadro mais severo.<br>

            </div>
            <div><h3>  17. Síndrome do olho seco</h3>   


                Também chamada de síndrome de disfunção lacrimal, é um defeito na composição das lágrimas ou na sua   <br>
                produção, o que prejudica a lubrificação dos olhos.<br>
                As pessoas que têm o chamado “olho seco”, assim, sentem uma pontada muito forte nos olhos, que passa <br>
                sozinho. No entanto, é preciso marcar uma consulta com um oftalmologista para que ele possa orientar <br>
                o tratamento mais adequado. Geralmente, ele é feito com o uso de colírios especiais.<br>
            </div>

            <div><h3>   18. Terçol</h3>   


                O terçol é outra do rol das doenças dos olhos mais comuns que existem. Ele acontece quando uma das <br>
                glândulas da borda das pálpebras ficam entupidas ou são infectadas por alguma bactéria. Neste último <br>
                caso, a doença é contagiosa.<br>
                Quando o terçol ocorre nas glândulas externas do olho, chamamos o problema de hordéolo. Já quando <br>
                afeta as glândulas internas, é um calázio, que não é infeccioso e causa sintomas mais amenos.<br>
                A blefarite não tratada pode provocar um quadro de terçol. O tratamento geralmente é feito com o <br>
                uso de pomadas, mas em alguns casos (especialmente quando o problema atinge a parte interna dos olhos) <br>
                é necessário remover por meio de uma cirurgia.<br>
            </div>

            <div><h3>   19. Uveíte</h3>  


                A uveíte é uma inflamação que afeta total ou parcialmente a úvea, parte do olho composta pela íris, <br>
                corpo ciliar e coroide. Em casos mais raros, pode atingir também a retina e o nervo óptico.<br>
                Apesar das causas desta doença dos olhos não ser totalmente conhecida, sabe-se que infecções por vírus, <br>
                bactérias ou fungos possam ter algo a ver. Outras doenças, como toxoplasmose, herpes simples, <br>
                citomegalovírus, tuberculose e sífilis também podem estar relacionadas à uveíte.<br>
                Ela é caracterizada principalmente pela vermelhidão que provoca nos olhos, mas também pode causar dores, <br>
                fotofobia, visão turva e embaçada e levar ao surgimento de pequenos pontos pretos na visão que se movimentam <br>
                rapidamente.
            </div>


