                       As 19 doenças dos olhos mais comuns que existem



                       
         1. Ambliopia

         Também chamada de “olho preguiçoso”, a ambliopia é uma disfunção oftalmológica  
sem lesão aparente. 
         Ela geralmente acontece em crianças e é caracterizada como uma doença que
prejudica a visão, mas que não pode ser corrigido somente com o uso de óculos.
         O tratamento é demorado, pois muitas vezes o problema não está restrito ao olho 
afetado, e sim à região cerebral responsável pela visão.
         Por afetar principalmente crianças, é comum que casos de ambliopia passem 
despercebidos pelos pais. Mas se não for tratado antes dos 7 ou 8 anos de idade, a doença
pode levar a um déficit de visão que durará pela vida toda. Por isso,acompanhamento 
oftalmológico é fundamental até mesmo nos primeiros anos de vida.

               

          2. Astigmatismo

            O astigmatismo é uma das doenças dos olhos mais comuns que existem. Ele é resultado  
de um problema na formação da córnea, que faz com que a imagem que deveria ser formada na retina
seja formada em diversas regiões diferentes do olho, resultando numa visão distorcida.
            Isso pode ser tanto um problema que acontece naturalmente, quanto um problema
associadoa algum trauma que a pessoa tenha sofrido no olho, como uma cicatrização, que altera a 
refração da luz.
            Além do uso de óculos (recomendado para a maioria dos casos), é possível realizar um
transplante de córnea para lidar com casos mais sérios.


          3. Blefarite

            A blefarite é uma inflamação nas pálpebras. Sua manifestação e severidade dependem 
bastante de cada pessoa, mas quem tem pele muito oleosa ou tendência a desenvolver caspas, por 
exemplo, pode sofrer com a blefarite.
            Mas o que blefarite tem a ver com caspa? A doença causa uma irritação na base dos cílios,
onde acabam sendo produzidas escamas muito semelhantes às caspas.
            Essa irritação e incômodo constantes podem afetar o resto do olho, portanto é fundamental
tratá-la com limpezas recorrentes, compressas de água morna ou remédios receitados pelo médico.
            Eventualmente, xampus com pH neutro podem dar conta do recado também.
            Lentes de contato sujas também podem causar blefarite. Então, caso você use lentes, 
mantenha-as sempre bem limpas.
