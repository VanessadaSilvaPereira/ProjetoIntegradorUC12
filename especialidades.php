    . (incisões, capsulorrexe, nucleofragmentação)
    . com benefício de dar mais precisão, previsibilidade e reprodutibilidade a cirurgia de catarata.'

    : Reconstituição do globo ocular: Os procedimentos cirúrgicos que ajudam a recuperar a superfície ocular são:

    TRANSPLANTE DE CÉLULAS TRONCO (TRANSPLANTE DE LIMBO)

    Neste procedimento levamos células tronco sadias do olho não afetado (doenças unilaterais),
    e nos casos bilaterais podemos retirar este tecido de um olho doador de um parente ou de um cadáver.  
    O doador vivo mais adequado é escolhido através de um exame de sangue (Compatibilidade HLA e ABO)  
    entre os irmãos, pais ou filhos do paciente afetado. O limbo, local onde estão as células tronco, 
    que é retirado do olho doador sadio não prejudica o doador pois a quantidade retirada é pequena.

    Nos casos de doador cadáver é necessário utilizar medicamentos imunossupressores via oral para evitar
    a rejeição. Quando se consegue um bom pareamento HLA pode-se evitar a imunossupressão.

    Trata-se de um procedimento ambulatorial realizado com anestesia local. Algumas vezes um futuro
    transplante de córnea é necessário para reabilitar a visão.

    TRANSPLANTE DE MEMBRANA AMNIÓTICA

    A membrana amniótica é previamente preparada a partir de uma placenta humana, e passa por um 
    processamento especial antes de ser utilizada. Esta membrana tem propriedades anti-inflamatórias,
    consegue diminuir os vasos da córnea e facilita a cicatrização. Pode ser usada isoladamente nas 
    queimaduras agudas, nas perdas parciais das células tronco, no alívio da dor de certos edemas de 
    córnea, ou nos casos de úlceras de córnea que não respondem ao tratamento convencional.

    Também pode ser associada ao transplante de limbo, principalmente para refazer as áreas internas
    das pálpebras e melhorar as condições gerais do olho.




    Dr. Vitor    : Especilista em cirurgia de retina: A retina é uma fina camada de células que reveste a parte mais interna do olho. Ela é
    responsável por transformar o estímulo luminoso num estímulo neurológico que é transmitido
    ao nervo óptico. Por sua vez, o nervo óptico leva estas informações para o cérebro que,
    após seu o processamento, se transforma no que chamamos de visão. A retina ainda não pode
    ser transplantada ou “trocada”, por isso qualquer dano que ocorra com ela, pode ser irreversível.
    Doenças da retina podem ser graves e, se não tratadas a tempo, podem levar a uma cegueira irrecuperável.

    Muitas doenças sistêmicas como o diabetes e a hipertensão arterial podem causar danos na retina
    e provocar perda de visão.

    O oftalmologista especializado em retina (chamado também de retinólogo) é um profissional que faz 
    seis anos de medicina, mais três de oftalmologia e, geralmente faz mais dois anos se dedicando a retina,
    para depois por estar apto a atender os casos desta especialidade dentro da oftalmologia.

